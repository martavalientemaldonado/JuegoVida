package com.example.juegovida.Clases.Recursos;

public class Tesoro {
    double ProbAparicion;
    double ProbReproduccionPropo;

    public Tesoro() {
    }

    public double getProbAparicion() {
        return ProbAparicion;
    }

    public void setProbAparicion(double probAparicion) {
        this.ProbAparicion = probAparicion;
    }

    public double getProbReproduccionPropo() {
        return ProbReproduccionPropo;
    }

    public void setProbReproduccionPropo(double ProbReproduccionPropo) {
        this.ProbReproduccionPropo = ProbReproduccionPropo;
    }
}
